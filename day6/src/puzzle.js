// 1: [] + []
console.log("Puzzle 1: [] + []");
console.log([] + []); 

// 2: {} + []
console.log("\nPuzzle 2: {} + []");
console.log({} + []); 

// Forced object context 
console.log("Forced: ({} + [])");
console.log(({} + [])); 

// 3: [] + {}
console.log("\nPuzzle 3: [] + {}");
console.log([] + {}); 

// 4: {} + {}
console.log("\nPuzzle 4: {} + {}");
console.log({} + {}); 

// Force object context with parentheses
console.log("Forced: ({} + {})");
console.log(({} + {})); 

// 5: true + false
console.log("\nPuzzle 5: true + false");
console.log(true + false); 
